hold on;%表示多个图线画在同一张图上
x=[1,1];y=[1,4];plot(x,y,‘blue’)%（1,1）和（1,4）两点连线， 竖线
hold on;
x=[1,2];y=[4,4];plot(x,y,‘blue’)%（1,4）和（2,4）两点连线
hold on;
x=[2,2];y=[2,4];plot(x,y,‘blue’)%同理
hold on;
x=[2,3];y=[2,2];plot(x,y,‘blue’)
hold on;
x=[3,3];y=[1.5,2];plot(x,y,‘blueC’)%C代表虚线
